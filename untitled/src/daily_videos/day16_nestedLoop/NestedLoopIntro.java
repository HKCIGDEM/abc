package daily_videos.day16_nestedLoop;

public class NestedLoopIntro {
    public static void main(String[] args) {

        for (int i = 0; i<5; i++){
            System.out.println("WoodenSpoon");
        }
        for (int i = 0; i<5; i++){
            System.out.println("WoodenSpoon");
        }
        for (int i = 0; i<5; i++){
            System.out.println("WoodenSpoon");
        }
        for (int i = 0; i<5; i++){
            System.out.println("WoodenSpoon");
        }
        for (int i = 0; i<5; i++){
            System.out.println("WoodenSpoon");
        }
        System.out.println("---------------------------------");

        for (int i = 0; i<5; i++) { // i : 0

            for (int j = 0; j<5; j++){
                System.out.println("WoodenSpoon");
            }

        }


    }

}
